    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String codigo = campoCodigo.getText();
        String nombre = campoNombre.getText();
        String precio = campoPrecio.getText();
        Producto temporal = new Producto (Integer.valueOf(codigo), nombre, Integer.valueOf(precio));

        if (!codigo.equals("") && !nombre.equals("") && !precio.equals("") ) {

            boolean proceso =this.ventanaMenu.database.eliminarProducto(temporal);
            Alert alerta =new Alert ("EXITO","Producto Eliminada", "succes");

            campoCodigo.setText("");
            campoNombre.setText("");
            campoPrecio.setText("");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

