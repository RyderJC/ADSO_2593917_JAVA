    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String cedula = campoCedula.getText();
        String nombre = campoNombre.getText();
        String apellidos = campoApellidos.getText();
        String direccion = campoDireccion.getText();
        String telefono = campoTelefono.getText();
        String email = campoEmail.getText();
        Persona temporal = new Persona (cedula, nombre, apellidos, direccion, telefono, email);

        if (!cedula.equals("") && !nombre.equals("") && !apellidos.equals("") && !direccion.equals("") && !telefono.equals("") && !email.equals("") ) {

            boolean proceso =(this.tipoLista.equalsIgnoreCase("CLIENTES"))? this.ventanaMenu.database.eliminarCliente(temporal): this.ventanaMenu.database.eliminarVendedor(temporal);
            Alert alerta =new Alert ("EXITO","Persona Eliminada", "succes");

            campoCedula.setText("");
            campoNombre.setText("");
            campoApellidos.setText("");
            campoDireccion.setText("");
            campoTelefono.setText("");
            campoEmail.setText("");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

